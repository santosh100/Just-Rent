    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        saveButton.setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
        String sname, fname, mobno, join_dt, resign_dt, address;
        sname = nameTextField.getText().trim().toUpperCase();
        fname = fnameTextField.getText().trim().toUpperCase();
        mobno = mobnoTextField.getText().trim();
        address = addTextArea.getText().trim().toUpperCase();
        DateFormat df;
        df = DateFormat.getDateInstance(DateFormat.MEDIUM, Locale.UK);
        if (sname.equalsIgnoreCase("") || fname.equalsIgnoreCase("") || mobno.equals("") || address.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "FILL ALL DETAILS CAREFULLY (Except Id)", "SAVE RECORD", JOptionPane.ERROR_MESSAGE);
        } else if (mobno.length() != 10) {
            JOptionPane.showMessageDialog(null, "MOBILE  NO CANNOT BE LESS THAN 10 DIGITS)", "SAVE RECORD", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                resign_dt = df.format(resignDateChooser.getDate());
            } catch (Exception e) {
                System.out.println("" + e);
                resign_dt = "-";
            }
            try {
                join_dt = df.format(joinDateChooser.getDate());

                pstmt = MyLogin.con.prepareStatement("insert into STAFF_DETAIL (S_NAME,S_F_NAME,S_MOB_NO,S_JOIN_DT,S_REGN_DT,S_ADDRESS) values(?,?,?,?,?,?)", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                pstmt.setString(1, sname);
                pstmt.setString(2, fname);
                pstmt.setString(3, mobno);
                pstmt.setString(4, join_dt);
                pstmt.setString(5, resign_dt);
                pstmt.setString(6, address);
                int i = pstmt.executeUpdate();
                pstmt.clearParameters();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "Entry successfull", "Staff Entry", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                    addRecordIntoTable();
                } else {
                    System.out.println("Problem in insertion data in DEALER_DETAIL TABLE");
                }
            } catch (Exception e) {
                System.out.println("Error " + e);
                JOptionPane.showMessageDialog(null, "FILL ALL DETAILS CAREFULLY (Except Id)", "SAVE RECORD", JOptionPane.ERROR_MESSAGE);
            }
        }
        saveButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
    }//GEN-LAST:event_saveButtonActionPerformed

