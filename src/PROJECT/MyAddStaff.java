    public void addRecordIntoTable() {
        pstmt = null;
        rs = null;
        try {
            String s_name = searchTextField.getText().trim().toUpperCase();
            if (s_name.equalsIgnoreCase("")) {
                pstmt = MyLogin.con.prepareStatement("select * from STAFF_DETAIL", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            } else {
                pstmt = MyLogin.con.prepareStatement("select * from STAFF_DETAIL where S_NAME like '" + s_name + "%'", ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            }
            rs = pstmt.executeQuery();
            pstmt.clearParameters();

            DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
            while (tm.getRowCount() > 0) {
                tm.removeRow(0);
            }
            while (rs.next()) {
                Object kk[] = {rs.getInt("S_ID"), rs.getString("S_NAME"), rs.getString("S_F_NAME"), rs.getString("S_MOB_NO"), rs.getString("S_JOIN_DT"), rs.getString("S_REGN_DT"), rs.getString("S_ADDRESS")};
                tm.addRow(kk);
            }
        } catch (Exception e) {
            System.out.println(" Error inside addrecordinto table " + e);
        }
    }

